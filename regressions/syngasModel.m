function y = nonLinearModel(a,x)

lnb1   = a(1);
nCO    = 1;
nlow   = a(2);
nhigh  = a(3);
b2     = a(4);

PH2 = x(:,2);
PCO = x(:,1);

y = lnb1 + nCO*log(PCO) + nlow*log(PH2) - log(1+b2*PH2.^nhigh);

end


